// Brew log for /coffee page.
// Add a new object to `brews` after each shot and commit.
// Flow rate and days-off-roast are derived at build time from this data.

export type Method = 'espresso' | 'filter' | 'other';

export type BrewEntry = {
  date: string;        // ISO date of brew (YYYY-MM-DD)
  bean: string;        // display name
  roaster?: string;
  roastDate: string;   // ISO date (YYYY-MM-DD)
  doseG: number;       // input dry coffee, grams
  yieldG: number;      // output in cup, grams
  timeS: number;       // pull time in seconds, from first drop hitting the cup
  grind?: string;      // grinder setting, free text (e.g. "DF64 #7")
  basket?: string;     // e.g. "DEX 15g"
  temp?: string;       // e.g. "Mid" on MaraX
  puckScreen?: boolean;
  method?: Method;     // defaults to 'espresso'
  rating?: 1 | 2 | 3 | 4 | 5;
  notes?: string;

  // Excludes the shot from the chart's regression / daily-mean math. Absent
  // means clean. Choose by *originating cause*, not by how the shot tasted:
  //   dial-in        — intentional grind / temp sweep before locking a setting
  //   process-error  — equipment step skipped or failed (no puck screen, scale
  //                    died mid-pour, machine cycled mid-shot, etc.)
  //   prep-error     — puck prep or handling mistake (channeling, fines,
  //                    spilled grounds and re-ground, lost bean, etc.)
  // Tainted shots still render on the chart as small × markers in the bean's
  // color and still appear in the log table — the chart just doesn't count
  // them toward the daily mean or the degradation fit.
  flag?: 'dial-in' | 'process-error' | 'prep-error';
};

// TODO: introduce a stable `bagId` (default: slug(bean) + '-' + roastDate) and
// switch chart data attrs, log row data attrs, dropdown values, and the
// shot-count map off of `bean` and onto `bagId`. Without this, buying a second
// bag of the same bean (different roast date) will conflate them in the UI.
export type Bag = {
  bean: string;                 // display name. Country-only for single origins
                                // ("Guatemala", "Ethiopia"), distinctive name for
                                // blends ("Mokha Java", "Dulzura"). Also acts as
                                // the bag's identity / key for filtering.
  roaster?: string;             // shown small/dim under the name
  roastDate: string;            // ISO

  // Lifecycle dates. Combination determines which optgroup the dropdown puts
  // this bag into:
  //   no openedDate, no closedDate  → Coming soon (bought but unopened)
  //   openedDate set,  no closedDate → Open
  //   closedDate set                 → Past
  openedDate?: string;
  closedDate?: string;          // when the bag was finished.

  // Identification attributes. All optional. Rendered as a small-caps badge row
  // in this canonical order, dropping any that are unset:
  //   special release · type · process · roast level
  // Use the bagBadges() helper to derive the row — never hand-write it.
  specialRelease?: boolean;
  type?: 'single origin' | 'blend' | 'espresso blend';
  process?: 'washed' | 'honey' | 'natural' | 'anaerobic';
  roastLevel?: 'light' | 'light-medium' | 'medium' | 'medium-dark' | 'dark';

  // origin: one-line human-readable origin description.
  //   single origin → region/country (e.g. "El Progreso, Guatemala", "Yirgacheffe, Ethiopia")
  //   blend → mix description (e.g. "Brazil + Mexico", "Papua New Guinea + Ethiopia")
  // When the bean name is country-only, origin carries the regional precision.
  origin?: string;
  // tastingNotes: ordered, short flavor descriptors. Rendered dot-separated.
  tastingNotes?: string[];

  // Optional single-origin detail fields. Rendered in a small key:value table
  // below tasting notes (rows render only when set). Blends typically omit.
  producer?: string;            // farm or smallholder group
  elevation?: string;           // free-text MASL ("1,800 MASL", "1,700–1,800 MASL")
  varieties?: string[];         // cultivar names
  harvest?: string;             // month range ("April–May", "November–April")

  // chartColor: hex color used for this bag's series in the degradation chart
  // (and the matching legend swatch). Sampled from the bag's packaging so
  // each bean reads the same on the chart as in the real world. Optional —
  // falls back to the cycled palette if omitted.
  chartColor?: string;
};

/** Build the badge row in canonical order. Empty fields are skipped. */
export function bagBadges(b: Bag): string[] {
  const out: string[] = [];
  if (b.specialRelease) out.push('special release');
  if (b.type) out.push(b.type);
  if (b.process) out.push(b.process);
  if (b.roastLevel) out.push(b.roastLevel);
  return out;
}

